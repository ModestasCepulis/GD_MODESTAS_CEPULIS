import javax.swing.*;
import java.awt.*;

public class MyGui {


    public static void main(String[] args) {

        new MyGui();

    }

    public MyGui()
    {

        //Creates the Jframe window
        JFrame frame = new JFrame("Window test");
        //Sets the frame to be visible
        frame.setVisible(true);
        //This regulates the size of the frame
        frame.setSize(200,300);
        //This changes the background color of the frame

    }
}
